
public abstract class vehicle {
	String name;
	int cost;
	int peopleNum;
	int cargoNum;
	
	public String getName(String name){
		return name;
	}
	
	public int getCost(int cost){
		return cost;
	}
	
	public int getPeopleNum(int pnum){
		return pnum;
	}
	
	public int getCargoNum(int cnum){
		return cnum;
	}
}
